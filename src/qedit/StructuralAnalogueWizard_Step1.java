    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nextDialog == null) {
            JFrame jframe = QEditApp.getView().getFrame();
            nextDialog = new StructuralAnalogueWizard_Step2(jframe);
            int frameWidth = jframe.getWidth();
            int frameHeight = jframe.getHeight();
            int dialogWidht = nextDialog.getWidth();
            int dialogHeight = nextDialog.getHeight();
            int dialog_x = (frameWidth - dialogWidht) / 2;
            int dialog_y = (frameHeight - dialogHeight) / 2;
            nextDialog.setBounds(dialog_x, dialog_y, dialogWidht, dialogHeight);
            nextDialog.setPreviousDialog(this);
        }
        setVisible(false);
        nextDialog.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

